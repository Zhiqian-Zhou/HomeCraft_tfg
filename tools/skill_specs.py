PATH_DIMS = {"min": [3, 2, 3], "preferred": [10, 2, 5], "max": [30, 3, 12]}
ROOF_TOP_DIMS = {"min": [3, 2, 3], "preferred": [6, 3, 6], "max": [12, 5, 12]}

# All styles the project supports — every skill is style-agnostic, so we list
# them all so the LLM retrieval matches regardless of style filter.
ALL_STYLES = ["medieval", "rustic", "fantasy", "gothic", "renaissance",
              "modern", "minimalist", "japanese", "chinese", "mediterranean"]


def _room(name_id, name, desc, recipe, *, params=None, dims=None, kind="decorative"):
    return {
        "id": name_id, "name": name, "description": desc,
        "kind": kind, "family": recipe.split("_")[0],
        "skill_category": "room_decoration",
        **(dims or ROOM_DIMS), "recipe": recipe, "params": params or {},
    }


def _ext(name_id, name, desc, recipe, *, params=None, dims=None, kind="exterior"):
    return {
        "id": name_id, "name": name, "description": desc,
        "kind": kind, "family": recipe.split("_")[0],
        "skill_category": "exterior_feature",
        **(dims or EXT_PLOT_DIMS), "recipe": recipe, "params": params or {},
    }


def _roof(name_id, name, desc, recipe, *, params=None, kind="decorative"):
    return {
        "id": name_id, "name": name, "description": desc,
        "kind": kind, "family": "roof",
        "skill_category": "exterior_feature",
        **ROOF_TOP_DIMS, "recipe": recipe, "params": params or {},
    }


