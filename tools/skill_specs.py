"""Spec list for the universal-skills generator.

Each entry describes ONE universal skill (`tags.category="any"`) that the
pipeline's room_agent or exterior_agent may pick for ANY building type.
The generator (`tools/gen_skills.py`) reads SKILLS and emits two files
per entry:

    pipeline/skills/<id>.py     thin wrapper calling a primitive in
                                 pipeline/skills/_universal_lib.py
    rag/skills/<id>.json        RAG entry validating against
                                 rag/schema/skill_entry.schema.json

Spec fields (all required unless noted):

    id              kebab-friendly snake_case skill id (also Python module name)
    name            human-readable display name
    description     ≥ 10 char description for the RAG entry
    kind            'room' | 'furniture' | 'structural' | 'exterior' | 'decorative'
    family          informal grouper (floor/wall/ceiling/...) — also surfaces in
                    skill_category mapping
    skill_category  v4 BOT category — typically 'room_decoration' or
                    'exterior_feature'
    min, preferred, max
                    typical_dimensions in (w, h, d)
    recipe          name of a function in pipeline.skills._universal_lib
    params (opt)    dict of keyword args baked into the wrapper's call

Universality contract: the generated skills MUST NOT assume a specific
category or style. They only consume the AABB and Materials they are given.
"""
from __future__ import annotations

# ── shared dimension presets ────────────────────────────────────────────────
ROOM_DIMS = {"min": [4, 3, 4], "preferred": [8, 4, 8], "max": [16, 9, 16]}
SMALL_ROOM_DIMS = {"min": [3, 3, 3], "preferred": [6, 4, 6], "max": [12, 6, 12]}
